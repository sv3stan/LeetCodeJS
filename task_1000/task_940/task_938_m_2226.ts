/**
Вам дают 0-индексированные целочисленные массивы конфеты. Каждый элемент в массиве обозначает кучу конфет с размерными конфетами [i]. Вы можете разделить каждую кучу на любое количество подвалов, но вы не можете объединить две свай вместе. Вам также дается целое число k. Вы должны распределить куча конфет для детей K так, чтобы каждый ребенок получил одинаковое количество конфет. Каждому ребенку можно выделить конфеты только из одной кучи конфет, и некоторые кучи конфет могут остаться неиспользованными. Вернуть максимальное количество конфет, которые может получить каждый ребенок.
 */

function maximumCandies(candies: number[], k: number): number {
    if (k === 0) return 0;
    let low = 1;
    let high = Math.max(...candies);
    let result = 0;
    while (low <= high) {
        let mid = Math.floor((low + high) / 2);
        let count = candies.reduce((sum, c) => sum + Math.floor(c / mid), 0);
        if (count >= k) {
            result = mid;
            low = mid + 1;
        } else {
            high = mid - 1;
        }
    }
    return result;
}
